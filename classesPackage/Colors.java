package classesPackage;

// A static class for holding ANSI Strings for printing colorful deep messages :3

public class Colors {
  public static String RESET = "\u001B[0m";
  public static String BLACK = "\u001B[30m";
  public static String WHITE = "\u001B[37m";
  public static String GREEN = "\u001B[32m";
  public static String RED = "\u001B[31m";
  public static String BOLD = "\033[0;1m";
  public static String YELLOW = "\033[0;33m";
  public static String BLUE = "\033[0;34m";
  public static String PURPLE = "\033[0;35m";
  public static String CYAN = "\033[0;36m";
  public static String GREEN_BG = "\033[42m";
  public static String PURPLE_BG = "\033[45m";
  public static String CYAN_BG = "\033[46m";

  public static String CYAN_UNDERLINED = "\033[4;36m";
}


